// Simulate real-time price updates
export const simulatePriceUpdate = (stock: Stock): Stock => {
  // Generate random price movement between -3% to +3%
  const priceChange = (Math.random() - 0.5) * 0.06;
  const newPrice = stock.currentPrice * (1 + priceChange);

  return {
    ...stock,
    currentPrice: Math.round(newPrice * 100) / 100,
  };
};
